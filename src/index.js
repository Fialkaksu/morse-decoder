const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let message = [];

    expr = expr.match(/.{10}/g).forEach(encode => {
        if (encode.includes('*')) {
            message.push(' ');
        } else {
            encode = encode.match(/.{2}/g).filter(el => el !== '00').map(el => el === '10' ? '.' : '-');
            message.push(encode.join(''));
        }
    });
    
    return message.map(letter => letter === ' ' ? letter : MORSE_TABLE[letter]).join('');
}

module.exports = {
    decode
}